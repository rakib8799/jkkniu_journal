        <h3 data-aos="fade-up-right">Format of an Abstract</h4>
            <ul>
                <li data-aos="fade-right">
                    A title (English translation of Bengali title is mandatory in bracket).
                </li>
                <li data-aos="fade-right">
                    A brief background to the research area.
                </li>
                <li data-aos="fade-right">
                    The purpose and objectives of the study.
                </li>
                <li data-aos="fade-right">
                    Methodology of the research.
                </li>
                <li data-aos="fade-right">
                    Application of any specific literary/cultural/political/sociological theory.
                </li>
                <li data-aos="fade-right">
                    The significance/impact/novel contributions of the study in respective fields.
                </li>
                <li data-aos="fade-right">
                    Use SuttonyMJ for Bangla and Times New Roman (TNR) for English with 12 font size.
                </li>
                <li data-aos="fade-right">
                    Send MS Word file and a PDF file.
                </li>
                <li data-aos="fade-right">
                    Follow the APA 7th ed. reference style.
                </li>
                <li data-aos="fade-right">
                    Provide your academic/ professional affiliation and contact address.
                </li>
            </ul>
